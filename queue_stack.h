#ifndef QUEUE_STACK_H_INCLUDED
#define QUEUE_STACK_H_INCLUDED

typedef struct {
  int key;
} item;

typedef struct {
  item *data;
  int first, last;
} queue_;

typedef struct {
    item *data;
    int top;
} stack_;

/**********************************************************************************************
 * Queue
 *
 * Functions that operate on struct queue_
 * Queue is a FIFO list
 *
 **********************************************************************************************/

/**********************************************************************************************
 * New Queue
 *
 * Returns a new queue with space for n items allocated
 **********************************************************************************************/
queue_ * new_queue (int n);

/**********************************************************************************************
 * Empty Queue
 *
 * Returns 1 if queue is empty
 **********************************************************************************************/
int empty_queue (queue_ q);

/**********************************************************************************************
 * Put In Queue 
 *
 * Puts element in queue if queue is not full
 **********************************************************************************************/
void put_in_queue (int x, int m, queue_ *q);

/**********************************************************************************************
 * Remove From Queue
 *
 * Removes item from queue and return value removed
 **********************************************************************************************/
int remove_from_queue (int m, queue_ *q);

/**********************************************************************************************
 * Delete Queue
 *
 * Deletes queue and free space allocated
 **********************************************************************************************/
void delete_queue (queue_ *q);

/**********************************************************************************************
 * Stack
 *
 * Functions that operate on struct stack_
 * Queue is a LIFO list
 *
 **********************************************************************************************/

/**********************************************************************************************
 * New Stack
 *
 * Returns a new stack with space for n items allocated
 **********************************************************************************************/
stack_ * new_stack (int n);

/**********************************************************************************************
 * Empty Stack
 *
 * Returns 1 if stack is empty
 **********************************************************************************************/
int empty_stack (stack_ s);

/**********************************************************************************************
 * Push
 *
 * Pushes item to stack
 **********************************************************************************************/
void push (int x, int m, stack_ *s);

/**********************************************************************************************
 * Pop
 *
 * Pops item from stack returning value removed
 **********************************************************************************************/
int pop (int m, stack_ *s);

/**********************************************************************************************
 * Delete Stack
 *
 * Deletes stack and free space allocated
 **********************************************************************************************/
void delete_stack (stack_ *s);

#endif // QUEUE_STACK_H_INCLUDED